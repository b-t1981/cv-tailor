#!/usr/bin/env node
import { execSync } from "node:child_process";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const FRONTEND_ROOT = join(SCRIPT_DIR, "..");
const OUTPUT_PATH = join(FRONTEND_ROOT, "src", "generated", "build-info.ts");
const VERSION_RE = /^v?(\d+\.\d+(?:\.\d+)?)/;

function getRepoRoot() {
  try {
    return execSync("git rev-parse --show-toplevel", {
      cwd: FRONTEND_ROOT,
      encoding: "utf8",
    }).trim();
  } catch {
    return null;
  }
}

function firstLine(text) {
  return (text || "").split(/\r?\n/)[0].trim();
}

function readCommitEditMsg(repoRoot) {
  if (!repoRoot) return "";
  const path = join(repoRoot, ".git", "COMMIT_EDITMSG");
  if (!existsSync(path)) return "";
  const line = firstLine(readFileSync(path, "utf8"));
  if (!line || line.startsWith("#")) return "";
  return line;
}

function getCommitSubject({ preferEditMsg } = {}) {
  const ci =
    process.env.VERCEL_GIT_COMMIT_MESSAGE || process.env.RENDER_GIT_COMMIT_MESSAGE;
  if (ci?.trim()) return firstLine(ci);

  const repoRoot = getRepoRoot();

  if (preferEditMsg) {
    const fromEdit = readCommitEditMsg(repoRoot);
    if (fromEdit) return fromEdit;
  }

  if (repoRoot) {
    try {
      const subject = execSync("git log -1 --format=%s", {
        cwd: repoRoot,
        encoding: "utf8",
      }).trim();
      if (subject) return subject;
    } catch {
      /* not a git repo or no commits */
    }
  }

  if (!preferEditMsg) {
    const fromEdit = readCommitEditMsg(repoRoot);
    if (fromEdit) return fromEdit;
  }

  return "";
}

function getShortCommitHash(repoRoot) {
  if (!repoRoot) return "";
  try {
    return execSync("git rev-parse --short HEAD", {
      cwd: repoRoot,
      encoding: "utf8",
    }).trim();
  } catch {
    return "";
  }
}

function escapeTsString(value) {
  return value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function main() {
  const preferEditMsg = process.argv.includes("--prefer-edit-msg");
  const subject = getCommitSubject({ preferEditMsg });
  const versionMatch = subject.match(VERSION_RE);
  const version = versionMatch ? versionMatch[1] : "";
  const buildLabel = version ? `v${version}` : "";

  const repoRoot = getRepoRoot();
  const commit = getShortCommitHash(repoRoot);
  const builtAt = new Date().toISOString();

  const content = `// Auto-generated by scripts/write-build-info.mjs — do not edit manually.
export const APP_VERSION = "${escapeTsString(version)}";
export const APP_COMMIT = "${escapeTsString(commit)}";
export const APP_BUILT_AT = "${escapeTsString(builtAt)}";
export const APP_BUILD_LABEL = "${escapeTsString(buildLabel)}";
`;

  mkdirSync(dirname(OUTPUT_PATH), { recursive: true });
  writeFileSync(OUTPUT_PATH, content, "utf8");

  if (buildLabel) {
    console.log(`build-info.ts: ${buildLabel} (${commit || "no commit"})`);
  } else {
    console.log("build-info.ts: no version in commit subject (label hidden in UI)");
  }
}

main();
